public class clubs{
	private String real;
	private String barca;
	private String bayern;
	private String juve;
	
	
	public clubs(String real,String barca,String bayern,String juve){
		this.real=real;
		this.barca=barca;
		this.bayern=bayern;
		this.juve=juve;
		
	}

	public String getReal() {
		return real;
	}

	public void setReal(String real) {
		this.real = real;
	}

	public String getBarca() {
		return barca;
	}

	public void setBarca(String barca) {
		this.barca = barca;
	}

	public String getBayern() {
		return bayern;
	}

	public void setBayern(String bayern) {
		this.bayern = bayern;
	}

	public String getJuve() {
		return juve;
	}

	public void setJuve(String juve) {
		this.juve = juve;
	}
	

}